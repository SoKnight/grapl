package com.huskerdev.grapl.core.window

/**
 * Window styles
 */
enum class WindowStyle {
    /**
     * Default window style (with title bar, borders, shadow)
     */
    DEFAULT,

    /**
     * Without any decorations (title bar, borders, shadow)
     */
    UNDECORATED,

    /**
     * Without title bar, but with borders and shadow
     */
    NO_TITLEBAR
}